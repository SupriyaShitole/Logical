package LogicalPrograms;

public class SwappingTwonumber {

	public static void main(String[] args) {
		int a=10,b=20; //Exp a=20,b=10
		
		System.out.println("Before Swapping values:"+a+" "+b);
		
		int c=a;//10
		a=b;//20
		b=c;//10
		
		System.out.println("Before Swapping values:"+a+" "+b);

	}

}
